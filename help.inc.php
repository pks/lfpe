<p style="margin-top:-1em">Press the 'Next' button to <strong>submit</strong> your post-edit and to request the next segment for post-edition.
Alternatively, in the textual interface, you may just press return when you finished the post-edit ('Target' text area is in focus).</p>
<p>The session can be paused at any time and continued later; However, if you have to pause your session, wait until the activity notification disappears and then press 'Pause', as we are collecting timing information. You may also just reload this site and re-request the segment to reset the timer.</p>
<p>Please use only a <strong>single browser window</strong> at the same time. Going back to earlier examples is not possible, please take great care when interacting with the system.</p>

<p><span style="border-bottom:1px solid #ccc">Instructions for the graphical interface:</span></p>
<ul>
  <li><strong>Moving around:</strong> Press <strong>'S'</strong>, then move around using the arrow keys.</li>
  <li><strong>Reordering of target phrases:</strong> Press <strong>'M'</strong>, then use the arrow keys.</li>
  <li><strong>Adding/removing alignments:</strong> Select a source phrase by left-clicking on its box, then click on a suitable target phrase (click selected source phrase again to cancel).</li>
  <li><strong>Edit text:</strong> Double click on text, click outside once or press return to stop editing. Alternatively, press <strong>'E'</strong> to edit the contents of the current phrase.</li>
  <li><strong>Adding target phrases:</strong> Click '+' button to add a new phrase at the end of the translation. To add a phrase right next to the currently selected phrase just press <strong>'A'</strong>.</li>
  <li><strong>Removing target phrases:</strong> Alt-click a phrase and press <strong>'D'</strong> key. Selecting multiple phrases for deletion is also possible. To delete the currently selected phrase just press <strong>'D'</strong>.</li>
  <li><strong>Reset:</strong> Click 'Reset' button to start from scratch.</li>
</ul>

<p>The interface was tested with Firefox 31 and 38.</p>

